

def convert_number(num):
    print(f"Converting {num}")

    if num == "one":
        return 1
    if num == "two":
        return 2
    if num == "three":
        return 3
    if num == "four":
        return 4
    if num == "five":
        return 5
    if num == "six":
        return 6
    if num == "seven":
        return 7
    if num == "eight":
        return 8
    if num == "nine":
        return 9
    if num == "ten":
        return 10
    else:
        return int(num)